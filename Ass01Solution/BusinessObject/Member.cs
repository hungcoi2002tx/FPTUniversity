namespace BusinessObject
{
    public class Member
    {
		private int id;

		public int Id
		{
			get { return id; }
			set { id = value; }
		}

		private String name;

		public String Name
		{
			get { return name; }
			set { name = value; }
		}

		private String email;

		public String Email
		{
			get { return email; }
			set { email = value; }
		}

		private String pass;

		public String Pass
		{
			get { return pass; }
			set { pass = value; }
		}

		private String city;

		public String City
		{
			get { return city; }
			set { city = value; }
		}

		private String country;

		public String Country
		{
			get { return country; }
			set { country = value; }
		}


	}
}